#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*arr;

	arr = (char *)str;
	i = 0;
	while (i < n)
	{
		arr[i] = c;
		i++;
	}
	return (&arr[0]);
}
